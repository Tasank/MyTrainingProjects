"""
Упражнение 143. Анаграммы.
Анаграммами называются слова, образованные путем взаимной перестановки букв. В английском языке, например,
анаграммами являются слова «live» и «evil», а в русском – «выбор» и «обрыв». Напишите программу,
которая будет запрашивать у пользователя два слова, определять, являются ли они анаграммами, и выводить на экран ответ.
"""


def are_anagrams(word1, word2):
    # Приводим слова к нижнему регистру
    word1 = word1.lower()
    word2 = word2.lower()

    # Если длина слов не совпадает, они не могут быть анаграммами
    if len(word1) != len(word2):
        return False

    # Создаём словари для подсчёта количества каждой буквы
    count1 = {}
    count2 = {}

    # Заполняем словари
    for letter in word1:
        count1[letter] = count1.get(letter, 0) + 1

    for letter in word2:
        count2[letter] = count2.get(letter, 0) + 1

    # Сравниваем словари
    return count1 == count2


def main():
    # Запрашиваем у пользователя два слова
    word1 = input("Введите первое слово: ")
    word2 = input("Введите второе слово: ")

    # Проверяем, являются ли слова анаграммами
    if are_anagrams(word1, word2):
        print("Слова являются анаграммами.")
    else:
        print("Слова не являются анаграммами.")


# Запускаем основную функцию
if __name__ == "__main__":
    main()
