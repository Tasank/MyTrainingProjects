"""
Упражнение 178. Рекурсивные палиндромы.
Напишите основную программу, которая будет запрашивать у пользователя слово и при помощи рекурсивной функции определять,
 является ли оно палиндромом. В результате на экране должно появиться соответствующее сообщение.
"""
# Рекурсивная функция "Рекурсивные палиндромы"
def is_palindrome(word):
    # Базовый случай: если длина слова 0 или 1, это палиндром
    if len(word) <= 1:
        return True
    # Если первый и последний символы не совпадают, это не палиндром
    if word[0] != word[-1]:
        return False
    # Рекурсивный вызов с удалением первого и последнего символов
    return is_palindrome(word[1:-1])

def main():
    word = input("Введите слово: ").strip().lower()
    if is_palindrome(word):
        print("Это палиндром.")
    else:
        print("Это не палиндром.")

if __name__ == "__main__":
    main()

