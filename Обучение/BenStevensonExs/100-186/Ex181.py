"""
Упражнение 181. Возможный размен.
Напишите программу, которая будет определять, можно ли составить конкретную сумму из определенного количества монет.
Ваша основная программа должна запрашивать у пользователя искомую сумму и количество монет. На выходе вы должны
получить сообщение о том, можно или нет собрать введенную сумму при помощи заданного количества монет.
"""
def can_make_amount(amount, num_coins):
    # Набор номиналов монет
    coins = [1, 5, 10, 25]

    # Базовые случаи
    if amount == 0 and num_coins == 0:
        return True
    if amount < 0 or num_coins <= 0:
        return False

    # Проверяем все возможные номиналы монет
    for coin in coins:
        if can_make_amount(amount - coin, num_coins - 1):
            return True

    return False

def main():
    # Запрашиваем у пользователя сумму и количество монет
    amount = int(input("Введите сумму в центах: "))
    num_coins = int(input("Введите количество монет: "))

    # Проверяем, можно ли собрать сумму из заданного количества монет
    if can_make_amount(amount, num_coins):
        print(f"Можно собрать сумму {amount} центов из {num_coins} монет.")
    else:
        print(f"Нельзя собрать сумму {amount} центов из {num_coins} монет.")

if __name__ == "__main__":
    main()
