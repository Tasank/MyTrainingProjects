"""
Упражнение 177. Римские цифры.
Создайте рекурсивную функцию, которая будет переводить римскую запись чисел в десятичную.
Функция должна обрабатывать один или два символа в начале строки, после чего будет производиться ее рекурсивный вызов
для оставшихся символов. Используйте пустую строку с возвращаемым значением 0 в качестве базового случая.
Также напишите основную программу, которая будет запрашивать у пользователя число, введенное римскими цифрами,
и отображать его десятичный эквивалент. При этом можно сделать допуск о том, что пользователь всегда вводит
корректное число, так что обработку ошибок вам реализовывать не нужно.
"""
# Рекурсивная функция римских цифр
def roman_to_decimal(roman):
    roman_numerals = {
        'M': 1000, 'D': 500, 'C': 100, 'L': 50, 'X': 10, 'V': 5, 'I': 1
    }
    if not roman:
        return 0
    if len(roman) > 1 and roman_numerals[roman[0]] < roman_numerals[roman[1]]:
        return roman_numerals[roman[1]] - roman_numerals[roman[0]] + roman_to_decimal(roman[2:])
    else:
        return roman_numerals[roman[0]] + roman_to_decimal(roman[1:])

def main():
    roman_number = input("Введите число римскими цифрами: ").upper()
    decimal_number = roman_to_decimal(roman_number)
    print(f"Десятичный эквивалент: {decimal_number}")

if __name__ == "__main__":
    main()
