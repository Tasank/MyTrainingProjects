"""
Упражнение 31. Единицы давления.
В данном задании вам предстоит написать программу,
которая будет переводить введенное пользователем значение давления в килопаскалях (кПа)
в фунты на квадратный дюйм (PSI), миллиметры ртутного столба и атмосферы.
Коэффициенты и формулы для перевода найдите самостоятельно.
"""
# Запрашиваем давление
kPa = float(input('Введите давление в килопаскалях (кПа): '))

# Коэффициенты для перевода:
# 1 кПа = 0.145038 PSI
# 1 кПа = 7.50062 мм рт. ст.
# 1 кПа = 0.00986923 атм

# Переводим давление в фунты на квадратный дюйм (PSI)
PSI = kPa * 0.145038

# Переводим давление в миллиметры ртутного столба (мм рт. ст.)
mmHg = kPa * 7.50062

# Переводим давление в атмосферы (атм)
atm = kPa * 0.00986923

# Выводим результаты
print(f'Фунты на квадратный дюйм (PSI): {PSI:.2f}')
print(f'Миллиметры ртутного столба (мм рт. ст.): {mmHg:.2f}')
print(f'Атмосферы (атм): {atm:.5f}')